    private boolean ValidarCampos() {
        //MODIFICAR RETROALIMENTACI√ìN !!!!
        String url = FieldURL.getText();
        String tipoNoticia = FieldNoticia.getText();
        String numeroID = FieldNumeroID.getText();
        String descripcion = TextDescripcion.getText();

        if((url.length()==0)||(tipoNoticia.length()==0)||(numeroID.length()==0)||descripcion.length()==0){
            JOptionPane.showMessageDialog(null,"ERROR: Uno o m√°s campos est√°n vac√≠os.");
            return false;
        }
        if(!tipoNoticia.matches("[a-zA-Z]*")){
            System.out.println("tipo noticia : " + tipoNoticia);
            JOptionPane.showMessageDialog(null,"El campo Tipo noticia s√≥lo acepta letras");
            return false;
        }
        if(!numeroID.matches("[0-9]*")){
            JOptionPane.showMessageDialog(null,"El campo numero ID s√≥lo acepta numeros");
            return false;
        }
        return true;
    }

